package ar.org.utn.ddstpanual.model;

public class Periodo {

  private String fecha;
  private float valor;

  public String getFecha() {
    return fecha;
  }

  public void setFecha(String fecha) {
    this.fecha = fecha;
  }

  public float getValor() {
    return valor;
  }

  public void setValor(float valor) {
    this.valor = valor;
  }
}
